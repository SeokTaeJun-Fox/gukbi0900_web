// 겨울 간식 테스트
// 붕어빵 먹는 부위
// "머리", "몸통", "꼬리"
// 머리: 한입에 먹기 좋아서
// 몸통: 팥부터 먹고 싶어서
// 꼬리: 바삭바삭함이 좋아서

// 이유 출력하기

const eatStyle = "머리";

if (eatStyle === "머리")
    console.log("한입에 먹기 좋아서");
  else if (eatStyle === "몸통")
    console.log("팥부터 먹고 싶어서");
  else if (eatStyle === "꼬리")
    console.log("바삭바삭함이 좋아서");
  else
    console.log("독특한 성향이 되고 싶어서");
